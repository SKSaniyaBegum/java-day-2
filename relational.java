public class relational {
    public static void main(String[] args) {
        int a = 90;
        int b = 78;
        System.out.println(a = b);
        System.out.println(a - b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }

}
